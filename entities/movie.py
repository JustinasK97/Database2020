class movie:

    def __init__(self, movie_id, movie_title, movie_director, release_date, rating, genre, box_office_id, studio_id):
        self.movie_id = movie_id
        self.movie_title = movie_title
        self.movie_director = movie_director
        self.release_date = release_date
        self.rating = rating
        self.genre = genre
        self.box_office_id = box_office_id
        self.studio_id = studio_id